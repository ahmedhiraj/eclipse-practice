
public class Student {
private int id;
private String name;
private float marks;
public  Student(int x,String y,float z)
{
	super();
	id=x;
	name=y;
	marks=z;
	
}
public Student()
{
	
}
public int getId()
{
	return id;
}
public String getName()
{
	return name;
}
public float getMarks()
{
	return marks;
}
}
